package com.codewithsouma.basic;

public class RightShiftLeftShift {
    public static void main(String[] args){
        int number1 = 32;
        int number2 = 12;


        System.out.println("Right shift: "+ (number1 >> 3));// divided by 2 in three times
        System.out.println("Left shift: "+ (number2 << 2));// multiply by 2 in two times

    }
}
