package com.codewithsouma;

public class AssigmentOperator {
    public static void main(String[] args){
        int x = 3;
        int y = 2;
        x += y; // 5
        System.out.println(x);

        x-=y; // 3
        System.out.println(x);

        x*=y; // 6
        System.out.println(x);

        x/=y; // 3
        System.out.println(x);

        x*=y; // 6
        System.out.println(x);

    }
}
